public class AnsiCodes {
    // https://gist.github.com/fnky/458719343aabd01cfb17a3a4f7296797#sequences
    public static final char esc = 0x1B; // escape sequence (hex)
    public static final char csi = 0x9B; // control sequence introducer (hex)
    public static final char dcs = 0x90; // device control string (hex)
    public static final char osc = 0x9D; // operating system command (hex)
}
